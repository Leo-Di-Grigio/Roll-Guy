package game.cycle.scene.game.world.creature.struct;

public class Stats {

	public int strength;
	public int agility;
	public int stamina;
	public int perception;
	public int intelligence;
	public int willpower;
	
	public Stats(int value) {
		this.strength = value;
		this.agility = value;
		this.stamina = value;
		this.perception = value;
		this.intelligence = value;
		this.willpower = value;
	}
}